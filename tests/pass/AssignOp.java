public class AssignOp {

    public int assignMinus (int x, int y){
        return x -= y;
    }

    public int assignStar (int x, int y){
        return x *= y;
    }

    public int assignDiv (int x, int y){
        return x /= y;
    }
    
    public int assignRem (int x, int y){
        return x %= y;
    }
    
}
